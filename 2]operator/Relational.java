class Relational{

    public static void main(String[] args){

        int p=3,q=2,r=3;
        
        System.out.println(p==q);//false
         System.out.println(p==r);//true
        System.out.println(p!=q);//true
        System.out.println(p>q);//true
        System.out.println(p<q);//false
        System.out.println(p>=q);//true
        System.out.println(p<=q);//false
        System.out.println(p<=r);// true                         
                                 
    }
}